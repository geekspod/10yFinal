     		<h6>What type of professional is right for you?</h6>
     			<p class="many-pro">Many Chefs can make a menu, shop for the meal, cook it and even serve it or package it up to be enjoyed later.  That said, the cost to hire a Chef can vary greatly depending on many factors such as where you live, how many people are being served, the quality of the ingredients, the number of people required for the event, how many courses the meal is comprised of and much more.  In most cases you’re either choosing between Private Chefs and caterers.  Neither one is necessarily better than the other, but one of them are better for you and your needs.  We hope this guide will make it much easy for you to figure out which is best for you.</p>

     		<h5 class="local-com">Private Chefs</h5>	

     		<p class="many-pro">There are several Private Chefs that provide the growing list of food services we’ve grown accustomed to like meal prepping, dinner parties, catering, grocery shopping, cooking lessons, knife sharpening and daily meals.</p>
     		<p class="many-pro">Many Private Chefs operate by themselves which is a great setup for most.  It allows them to have freedom in their scheduling to be able to take whatever event they want without concern to the availability of others, it allows them to set their own pay rate and gives them the most control over the actual execution of the selected service. </p>

     		<p class="many-pro">Where it is more difficult for them, however, is the amount of people they can cater to.  At Savor The Passion we like to recommend that a Chef working by themselves not cook for more than 10 people at a time in order to ensure the highest quality.</p>
     		<p class="many-pro">Hiring Chefs that work alone is also great because its so much easier to arrange for them to cook for you and your guests when they don’t live locally often no even in the same time zone.  This will minimize costs of travel plus room and board.</p>
     		<h5 class="local-com">Caterers</h5>
     		<p class="many-pro">Oftentimes we have hosts reach out that want to have an event for more than ten people, but are sick of going to the same restaurants or someone that can’t go out to eat in a restaurant for medical reasons getting a Chef with a support staff is a great option.  These Chefs often refer to themselves as caterers.</p>

     		<p class="many-pro">These Chefs that act as caters often charge based on the amount of people that they serve at any given event as well as other things like required staff on hand & extra equipment required to serve a larger crowd of people like warming trays, servers, bartenders, ice.  Distance often plays a major role in determining how much your event will cost.  Depending on where you are often changes what the standard distance is (if not immediately) that you start getting charged a traveling fee.</p>
