     	      <p class="many-pro">There are so many reasons that people can use a Chef to meal prep.  Most often we see busy families that are simply strapped for time.  When a host needs a meal prep, they can contact a Private Chef where they will work together to build the perfect menu.  On average you can expect each serving of your meal to cost $10-$12. This service is best used with a local Chef that lives near you so that you don’t have to pay for traveling costs.</p>
     		<p class="many-pro">Tip: When first building a relationship with your Chef stay in the kitchen or a nearby room to watch your Chefs process while they prepare your meal.  Its up to you if & when you want to share a security code with your Chef to maximize your time while they cook for you.</p>

